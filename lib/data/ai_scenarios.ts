/**
 * Find the best matching scenario for a user query.
 * Returns the scenario with the highest match score, or null if no match.
 */
export function findScenario(query: string): AiScenario | null {
  const normalized = query.toLowerCase().trim();
  if (normalized.length < 3) return null;

  let bestMatch: { scenario: AiScenario; score: number } | null = null;

  for (const scenario of SCENARIOS) {
    for (const keyword of scenario.keywords) {
      if (normalized.includes(keyword.toLowerCase())) {
        const score = keyword.length; // longer keywords = more specific match
        if (!bestMatch || score > bestMatch.score) {
          bestMatch = { scenario, score };
        }
      }
    }
  }

  return bestMatch?.scenario ?? null;
}

