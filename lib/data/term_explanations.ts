// Helper — find explanation by name, fuzzy match
export function findExplanation(
  name: string,
  category?: 'type' | 'instrument',
): TermExplanation | null {
  if (category === 'type' || !category) {
    if (MEASURE_TYPE_EXPLAIN[name]) return MEASURE_TYPE_EXPLAIN[name];
  }
  if (category === 'instrument' || !category) {
    if (INSTRUMENT_EXPLAIN[name]) return INSTRUMENT_EXPLAIN[name];
  }
  return null;
}
